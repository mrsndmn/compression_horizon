"""Launch progressive-cramming jobs that recompute the trajectory-cluster runs at 50 samples.

The trajectory-shape analysis (Appendix~\\ref{app:trajectory_clusters}, generated by
``scripts/analyze_trajectory_clusters.py`` + ``scripts/paper/tables/trajectory_clusters.py``)
characterises whether per-sample progressive-cramming embedding trajectories are smooth low-dim
curves or punctuated "dwell-and-jump" walks. Four of its rows were measured on legacy *10-sample*
runs because no 50-sample equivalent existed:

  * scale trend:  SmolLM2-135M (lr 0.1), SmolLM2-360M (lr 0.1)
  * 1.7B LR sweep: SmolLM2-1.7B at lr 0.5 and lr 1.0

(The 1.7B lr 0.1 scale point and the Llama-3.1-8B point already have 50-sample runs, and the 1.7B
lr 0.1 run doubles as the lr-sweep lr 0.1 row -- so they are intentionally NOT relaunched here.)

This launcher submits exactly those four runs at ``--limit_dataset_items 50`` on
``LarryLovestein/pg19_1k`` so the whole appendix table can drop its 10-vs-50-sample caveat. Each run
is a plain progressive-cramming baseline (cross-entropy, 1 alignment layer, random0.02 init, seq
4096) identical to the existing runs except for the sample count -- so the base
``run_jobs_progressive.render_job`` produces byte-identical output-dir names
(``sl_4096_<model>_ds_pg19_1k_limit_50_lr_<lr>``), which is exactly what the analysis cache + table
generator key on.

Exposes the uniform launcher interface (EXPERIMENTS, render_job, job_desc_for, make_client,
submit_experiment) so a watcher can wait on / retry these jobs. Post-processing differs from the
progressive-table ablations (it regenerates the trajectory-cluster artifacts, not a progressive
table), so it has its own watcher: ``scripts/jobs/watch_trajectory_clusters_50samples.py``.

Usage:
    python scripts/jobs/run_jobs_trajectory_clusters_50samples.py --dry
    python scripts/jobs/run_jobs_trajectory_clusters_50samples.py
"""

import argparse
import os
import sys

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))

from mls.manager.job.utils import get_in_progress_jobs, training_job_api_from_profile  # noqa: E402
from run_jobs_progressive import LIMIT_DATASET_ITEMS  # noqa: E402
from run_jobs_progressive import render_job as render_job  # noqa: E402

PYTHON_PATH = "/workspace-SR004.nfs2/d.tarasov/envs/compression_horizon/bin/python"
AUTHOR_NAME = "d.tarasov"
BASE_IMAGE = "cr.ai.cloud.ru/aicloud-base-images/py3.12-torch2.7.0:0.0.41"
JOB_DESC_PREFIX = "CH: trajectory-clusters-50samp"

# Sanity: the base launcher constant must be 50 for the output-dir names to carry the
# ``_limit_50`` token the analysis cache + table generator expect.
assert LIMIT_DATASET_ITEMS == 50, f"expected LIMIT_DATASET_ITEMS=50, got {LIMIT_DATASET_ITEMS}"


def _baseline(model_checkpoint: str, learning_rate: float) -> dict:
    """A plain progressive-cramming baseline experiment (the shape base render_job expects)."""
    return {
        "model_checkpoint": model_checkpoint,
        "learning_rate": learning_rate,
        "loss_type": "cross_entropy",
        "num_alignment_layers": 1,
        "hybrid_alpha": None,
        "low_dim_projection": False,
        "low_dim_size": None,
    }


# The four runs that only existed at 10 samples. Order = scale rows first, then the LR-sweep rows.
EXPERIMENTS = [
    _baseline("HuggingFaceTB/SmolLM2-135M", 0.1),
    _baseline("HuggingFaceTB/SmolLM2-360M", 0.1),
    _baseline("HuggingFaceTB/SmolLM2-1.7B", 0.5),
    _baseline("HuggingFaceTB/SmolLM2-1.7B", 1.0),
]


def job_desc_for(exp_suffix: str) -> str:
    return f"{JOB_DESC_PREFIX} {exp_suffix} #{AUTHOR_NAME} #multimodal #notify_completed @mrsndmn"


def build_payload(experiment: dict, extra_options: dict, workdir: str | None = None):
    workdir = workdir or os.getcwd()
    cmd_args, exp_suffix, out_dir_name = render_job(experiment)
    script = f" cd {workdir} && {PYTHON_PATH} scripts/activation_distillation.py  {' '.join(cmd_args)}"
    payload = {
        "script": script,
        "job_desc": job_desc_for(exp_suffix),
        "env_variables": {
            "PYTHONPATH": "./src",
            "HF_HOME": "/workspace-SR004.nfs2/.cache/huggingface",
        },
        "instance_type": "a100.1gpu",
        "queue_name": "fusionbrainlab-job",
        "region": extra_options["region"],
        "type": "binary_exp",
        "shm_size_class": "medium",
        "base_image": BASE_IMAGE,
        "n_workers": 1,
        "processes_per_worker": 1,
    }
    return payload, exp_suffix, out_dir_name


def make_client():
    return training_job_api_from_profile("default")


def submit_experiment(experiment, client, extra_options, in_progress_descs=None, dry=False, force=False):
    """Submit one experiment; return the ``run_job`` result dict or ``None`` if skipped."""
    payload, exp_suffix, out_dir_name = build_payload(experiment, extra_options)

    if not force and os.path.exists(out_dir_name):
        print("Experiment", out_dir_name, "exists, skip.")
        return None

    if in_progress_descs and payload["job_desc"] in in_progress_descs:
        print(f"\033[33mSkipping: job already in queue:\033[0m {payload['job_desc']}")
        return None

    print(f"\033[32m Would launch:\033[0m {payload['job_desc']}")
    print(f"\033[90m     Output dir: {out_dir_name}\033[0m")

    if dry:
        return None

    result = client.run_job(payload=payload)
    print(out_dir_name, result)
    return result


def main():
    parser = argparse.ArgumentParser(description="Launch the 50-sample trajectory-cluster recompute jobs.")
    parser.add_argument("--dry", action="store_true", help="Only print generated scripts, do not launch jobs.")
    args = parser.parse_args()

    client, extra_options = make_client()
    in_progress_descs = {job.get("job_desc", "") for job in get_in_progress_jobs()}

    for experiment in EXPERIMENTS:
        submit_experiment(experiment, client, extra_options, in_progress_descs, dry=args.dry)


if __name__ == "__main__":
    main()
